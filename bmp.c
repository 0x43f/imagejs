char *bmp_filename(char *fn, int size){
	const char _bmp_ending[BMP_ENDING] = {0x2e, 0x62, 0x6d, 0x70, 0x00};
	char *newfile = (char*)malloc(size + BMP_ENDING);
	memcpy(newfile, fn, size);
	memcpy(&newfile[size], _bmp_ending, BMP_ENDING);
	return newfile;
}
